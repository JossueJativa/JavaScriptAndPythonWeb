#Crear una lista de nombres
#Se usa corchetes para guardar los datos
#Se puede usar para guardar coordenadas
nombres = ["Harry", "Ron", "Hermione", "Ginny"]

#Agregar elementos a la lista
nombres.append("Sebas")
#sort es para poner elementos aleatorio en el programa
nombres.sort()
#imprimir la lista sin sort
print(nombres)

#Diccionarios se ocupa las llaves para poder imprimir los datos
#Se tiene un nombre y una llave, es decir Harry es la llave para que llame a lo que hay dentro de los dos puntos (:)
House = {"Harry": "Gryffindor", "Draco": "Slytherin"}
#Como se imprime:
print(House["Harry"])
#Donde House es la lista y Harry es la llave para llamar a los elementos

#Tuplas
#Se usa para guardar datos que no se van a cambiar
#Se usa parentesis para guardar los datos
#Se puede usar para guardar coordenadas
coordenadas = (10.0, 20.0)
#Se puede imprimir
print(coordenadas)

#Sets
#Se usa para guardar datos que no se van a repetir
#Se usa corchetes para guardar los datos
#Se puede usar para guardar coordenadas
coordenadas = [10.0, 20.0, 10.0]
#Se puede imprimir
print(coordenadas)
